package section3;

public class variable {
    public static void main(String[] args){
        // 데이터형 변수 = 상수;
        boolean bVar = true;
        int nVar = 12;
        char cVar = 'k';
        float fVar = 3.14f;

        System.out.println(bVar);
        System.out.println(nVar);
        System.out.println(cVar);
        System.out.println(fVar);
    } 
}
